class Evento:
    def __init__(self, idEvento, idArtista, nombre, descripcion, idSede, fecha, hora, cantidadBoletosNormales, cantidadBoletosVip):
        self.idEvento = idEvento
        self.idArtista = idArtista
        self.nombre = nombre
        self.descripcion = descripcion
        self.idSede = idSede
        self.fecha = fecha
        self.hora = hora
        self.cantidadBoletosNormales = cantidadBoletosNormales
        self.cantidadBoletosVip = cantidadBoletosVip